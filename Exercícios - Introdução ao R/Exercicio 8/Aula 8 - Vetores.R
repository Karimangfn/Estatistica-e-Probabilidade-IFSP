#Vetores - Estrutura básica de dados
#Uma sequencia de dados do mesmo tipo

#Vetor de Caracteres
Nomes <- c("Fernando","Maria","Reginaldo")

#Função is.vector confere se a variavel é um vetor
is.vector(Nomes) 
mode(Nomes)

#Vetor Numérico
Numeros <- c("100","200","300")
is.vector(Numeros)
mode(Numeros)

#Vetor Lógico
L1 <- TRUE
is.vector(L1)
mode(L1)

